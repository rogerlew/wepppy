from __future__ import annotations

import logging
import os
import subprocess
from datetime import datetime
from pathlib import Path

LOGGER = logging.getLogger(__name__)

_CONTROLLERS_GL_BUILD_ID_CACHE: dict[str, tuple[int, int, str | None]] = {}


def resolve_asset_version() -> str:
    """Return a stable asset version string for cache busting."""
    env_version = os.getenv("ASSET_VERSION")
    if env_version:
        return env_version.strip()

    repo_root = Path(__file__).resolve().parents[3]
    try:
        git_version = subprocess.check_output(
            ["git", "rev-parse", "--short", "HEAD"],
            cwd=repo_root,
            text=True,
        ).strip()
        if git_version:
            return git_version
    except Exception:
        LOGGER.debug("Falling back to timestamp for asset version", exc_info=True)

    return datetime.utcnow().strftime("%Y%m%d%H%M%S")


def resolve_controllers_gl_build_id(path: Path) -> str | None:
    """Return the build id embedded in controllers-gl.js, or None if unavailable.

    Reads the header block generated by `build_controllers_js.py` and extracts
    the `Build date:` line. Callers should treat None as "unknown" and avoid
    triggering stale-client UX based solely on a missing value.
    """
    cache_key = str(path)

    try:
        stat = path.stat()
    except OSError:
        return None

    cached = _CONTROLLERS_GL_BUILD_ID_CACHE.get(cache_key)
    if cached and cached[0] == stat.st_mtime_ns and cached[1] == stat.st_size:
        return cached[2]

    build_id: str | None = None
    try:
        with path.open("r", encoding="utf-8", errors="replace") as handle:
            for _ in range(80):
                line = handle.readline()
                if not line:
                    break
                if "Build date:" not in line:
                    continue
                build_id = line.split("Build date:", 1)[-1].strip()
                if build_id:
                    break
                build_id = None
                break
    except OSError:
        build_id = None

    _CONTROLLERS_GL_BUILD_ID_CACHE[cache_key] = (stat.st_mtime_ns, stat.st_size, build_id)
    return build_id
