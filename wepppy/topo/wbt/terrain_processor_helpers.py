def _lookup_hierarchy_column(
    available_columns: Mapping[str, str],
    aliases: Sequence[str],
) -> str | None:
    for alias in aliases:
        match = available_columns.get(alias)
        if match is not None:
            return match
    return None


def _parse_required_int(raw_value: str, *, field_name: str, row_num: int) -> int:
    value = str(raw_value).strip()
    if not value:
        raise ValueError(f"Row {row_num}: {field_name} cannot be empty")
    return int(value)


def _parse_optional_parent_id(raw_value: str | None, *, row_num: int) -> int | None:
    if raw_value is None:
        return None
    value = str(raw_value).strip()
    if value == "":
        return None
    parsed = int(value)
    if parsed in {0, -1}:
        return None
    if parsed < -1:
        raise ValueError(f"Row {row_num}: parent_basin_id cannot be less than -1")
    return parsed


def _parse_required_float(raw_value: str, *, field_name: str, row_num: int) -> float:
    value = str(raw_value).strip()
    if not value:
        raise ValueError(f"Row {row_num}: {field_name} cannot be empty")
    return float(value)


def _parse_optional_float(raw_value: str | None) -> float | None:
    if raw_value is None:
        return None
    value = str(raw_value).strip()
    if value == "":
        return None
    return float(value)


def _parse_optional_nonnegative_int(
    raw_value: str | None,
    *,
    field_name: str,
    row_num: int,
) -> int | None:
    if raw_value is None:
        return None
    value = str(raw_value).strip()
    if value == "":
        return None
    parsed = int(value)
    if parsed < 0:
        raise ValueError(f"Row {row_num}: {field_name} cannot be negative")
    return parsed


def parse_unnest_basins_hierarchy_csv(hierarchy_csv_path: str | Path) -> list[BasinSummary]:
    """Parse and validate hierarchy CSV output from `wbt.unnest_basins`."""
    path = Path(hierarchy_csv_path)
    if not path.exists():
        raise FileNotFoundError(f"Hierarchy CSV does not exist: {path}")

    with path.open(newline="") as fp:
        reader = csv.DictReader(fp)
        fieldnames = reader.fieldnames
        if not fieldnames:
            raise ValueError("Hierarchy CSV is missing a header row")

        normalized = {name.strip().lower(): name for name in fieldnames}

        required_columns: dict[str, str] = {}
        for field_name, aliases in _REQUIRED_HIERARCHY_COLUMN_ALIASES.items():
            match = _lookup_hierarchy_column(normalized, aliases)
            if match is None:
                raise ValueError(
                    f"Hierarchy CSV missing required column '{field_name}'. "
                    f"Accepted aliases: {aliases}"
                )
            required_columns[field_name] = match

        optional_columns: dict[str, str] = {}
        for field_name, aliases in _OPTIONAL_HIERARCHY_COLUMN_ALIASES.items():
            match = _lookup_hierarchy_column(normalized, aliases)
            if match is not None:
                optional_columns[field_name] = match

        rows = list(reader)

    basins: list[BasinSummary] = []
    seen_basin_ids: set[int] = set()
    for row_index, row in enumerate(rows, start=2):
        basin_id = _parse_required_int(
            row[required_columns["basin_id"]],
            field_name="basin_id",
            row_num=row_index,
        )
        if basin_id in seen_basin_ids:
            raise ValueError(f"Row {row_index}: duplicate basin_id {basin_id}")
        seen_basin_ids.add(basin_id)

        parent_basin_id = _parse_optional_parent_id(
            row.get(optional_columns.get("parent_basin_id", ""))
            if "parent_basin_id" in optional_columns
            else None,
            row_num=row_index,
        )

        outlet_x = _parse_required_float(
            row[required_columns["outlet_x"]],
            field_name="outlet_x",
            row_num=row_index,
        )
        outlet_y = _parse_required_float(
            row[required_columns["outlet_y"]],
            field_name="outlet_y",
            row_num=row_index,
        )

        area = _parse_optional_float(row.get(optional_columns.get("area", "")) if "area" in optional_columns else None)
        stream_order = _parse_optional_nonnegative_int(
            row.get(optional_columns.get("stream_order", ""))
            if "stream_order" in optional_columns
            else None,
            field_name="stream_order",
            row_num=row_index,
        )

        basins.append(
            BasinSummary(
                basin_id=basin_id,
                parent_basin_id=parent_basin_id,
                outlet_x=outlet_x,
                outlet_y=outlet_y,
                area=area,
                stream_order=stream_order,
            )
        )

    valid_ids = {record.basin_id for record in basins}
    for record in basins:
        parent_id = record.parent_basin_id
        if parent_id is None:
            continue
        if parent_id not in valid_ids:
            raise ValueError(
                f"Basin {record.basin_id} references unknown parent basin {parent_id}"
            )

    return basins


