@dataclass(frozen=True)
class ProvenanceEntry:
    """Record a single helper step and resulting artifact."""

    step: str
    artifact: str
    parameters: dict[str, Any] = field(default_factory=dict)
    metadata: dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> dict[str, Any]:
        record: dict[str, Any] = {
            "step": self.step,
            "artifact": self.artifact,
        }
        if self.parameters:
            record["parameters"] = dict(self.parameters)
        if self.metadata:
            record["metadata"] = dict(self.metadata)
        return record


class TerrainArtifactRegistry:
    """Phase-keyed artifact registry for TerrainProcessor helper outputs."""

    def __init__(self) -> None:
        self._artifacts_by_phase: dict[str, dict[str, str]] = {}

    def register(self, *, phase: str, artifact_name: str, artifact_path: str) -> None:
        if not phase:
            raise ValueError("phase must be non-empty")
        if not artifact_name:
            raise ValueError("artifact_name must be non-empty")
        if not artifact_path:
            raise ValueError("artifact_path must be non-empty")

        phase_bucket = self._artifacts_by_phase.setdefault(phase, {})
        phase_bucket[artifact_name] = artifact_path

    def phase_artifacts(self, phase: str) -> dict[str, str]:
        return dict(self._artifacts_by_phase.get(phase, {}))

    def all_artifacts(self) -> dict[str, dict[str, str]]:
        return {phase: dict(artifacts) for phase, artifacts in self._artifacts_by_phase.items()}


def determine_invalidated_phases(
    *,
    changed_config_keys: Iterable[str],
    invalidation_rules: Mapping[str, Sequence[str]] | None = None,
) -> list[str]:
    """Map config deltas to phase invalidation list ordered by phase sequence."""
    rules = (
        DEFAULT_PHASE_INVALIDATION_RULES
        if invalidation_rules is None
        else invalidation_rules
    )

    invalidated: set[str] = set()
    for key in changed_config_keys:
        phases = rules.get(key)
        if phases is None:
            invalidated.update(PHASE_SEQUENCE)
            continue
        invalidated.update(phases)

    return [phase for phase in PHASE_SEQUENCE if phase in invalidated]


